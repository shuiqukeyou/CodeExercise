# python的变量名建议全小写并且使用"_"分割单词
first_message = " hello world "
print(first_message)
# 标题化，将单词首字母大写
print(first_message.title())
# 大写化
print(first_message.upper())
# 小写化
print(first_message.lower())
# \t 输出制表符
print("\tpython")
# \n 输出换行
print("\npython")
# 删除掉该字符串末尾的空格并返回，不修改原变量
print(first_message.rstrip())
# 删除掉该字符串首部的空格并返回，不修改原变量
print(first_message.lstrip())
# 删除掉该字符串两段的空格并返回，不修改原变量
print(first_message.strip())
